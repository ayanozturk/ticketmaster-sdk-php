<?php
namespace TicketMaster\Entity;

/**
 * Class Category
 * @package TicketMaster\Entity
 */
class Category
{
    /**
     * @var string
     */
    private $id;
    /**
     * @var string
     */
    private $name;
    /**
     * @var string
     */
    private $locale;
    /**
     * @var int
     */
    private $level;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     * @return Category
     */
    public function setId(string $id): Category
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return Category
     */
    public function setName(string $name): Category
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getLocale(): string
    {
        return $this->locale;
    }

    /**
     * @param string $locale
     * @return Category
     */
    public function setLocale(string $locale): Category
    {
        $this->locale = $locale;
        return $this;
    }

    /**
     * @return int
     */
    public function getLevel(): int
    {
        return $this->level;
    }

    /**
     * @param int $level
     * @return Category
     */
    public function setLevel(int $level): Category
    {
        $this->level = $level;
        return $this;
    }

}
